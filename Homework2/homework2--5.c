#include <stdio.h>

int main() {
    int A[2][3] = {{1, 2, 3}, {4, 5, 6}};

    // 尝试通过修改元素值并查看其他位置元素是否变化来判断存放方式
    // 先修改按行存放可能相邻的元素
    A[0][0] = 100;
    if (A[0][1]!= 2) {
        printf("可能是按列存放\n");
    } else {
        // 再修改按列存放可能相邻的元素
        A[0][0] = 1;
        A[1][0] = 100;
        if (A[0][0]!= 1) {
            printf("可能是按行存放\n");
        } else {
            printf("无法准确判断，可能需要更多复杂测试\n");
        }
    }

    return 0;
}
